package oop.inheritance;

public class Diagnosis {

    private String diadnos;
    private int dayLife;

    public String getDiadnos() {
        return diadnos;
    }

    public void setDiadnos(String diadnos) {
        this.diadnos = diadnos;
    }

    public int getDayLife() {
        return dayLife;
    }

    public void setDayLife(int dayLife) {
        this.dayLife = dayLife;
    }
}
